        uint Xl = 0, Xr = 0;
        unchecked
        {
            for (int i = 0; i < 16; i++) { Xl ^= P[i]; Xr = TT(Xl, S) ^ Xr; (Xl, Xr) = (Xr, Xl); }
            (Xl, Xr) = (Xr, Xl); Xr ^= P[16]; Xl ^= P[17];
        }
        Console.WriteLine($"firstenc dl={Xl:x8} dr={Xr:x8}");
    }

    static uint TT(uint w, uint[] S)
    {
        unchecked
        {
            return ((S[256 + ((w >> 8) & 0xff)] & 1u) ^ 32u)
                 + ((S[768 + ((w >> 24) & 0xff)] & 1u) ^ 32u)
                 + S[512 + ((w >> 16) & 0xff)]
                 + S[w & 0xff];
        }
    }

    void Encipher(ref uint xl, ref uint xr)
    {
        unchecked
        {
            uint Xl = xl, Xr = xr;
            for (int i = 0; i < 16; i++) { Xl ^= P[i]; Xr = TT(Xl, S) ^ Xr; (Xl, Xr) = (Xr, Xl); }
            (Xl, Xr) = (Xr, Xl);
            Xr ^= P[16]; Xl ^= P[17];
            xl = Xl; xr = Xr;
        }
    }

    void Decipher(ref uint xl, ref uint xr)
    {
        unchecked
        {
            uint Xl = xl, Xr = xr;
            for (int i = 17; i > 1; i--) { Xl ^= P[i]; Xr = TT(Xl, S) ^ Xr; (Xl, Xr) = (Xr, Xl); }
            (Xl, Xr) = (Xr, Xl);
            Xr ^= P[1]; Xl ^= P[0];
            xl = Xl; xr = Xr;
        }
    }

    void Init(byte[] key)
    {
        for (int i = 0; i < 18; i++) P[i] = BinaryPrimitives.ReadUInt32LittleEndian(Subkey.AsSpan(i * 4));
        for (int i = 0; i < 1024; i++) S[i] = BinaryPrimitives.ReadUInt32LittleEndian(Subkey.AsSpan(72 + i * 4));
        if (Environment.GetEnvironmentVariable("BF_DEBUG") == "1") Console.WriteLine($"[load] P0={P[0]:x8}");
        int j = 0;
        for (int i = 0; i < 18; i++)
        {
            uint d = 0;
            // LSB's blowfish_init takes `const int8* key` (SIGNED char): bytes >= 0x80
            // sign-extend into the fold. Must replicate or the key schedule diverges.
            for (int k = 0; k < 4; k++) { d = (d << 8) | (uint)(sbyte)key[j]; j = (j + 1) % key.Length; }
            P[i] ^= d;
        }
        if (Environment.GetEnvironmentVariable("BF_DEBUG") == "1") Console.WriteLine($"[xor]  P0={P[0]:x8}");
        uint dl = 0, dr = 0;
        for (int i = 0; i < 18; i += 2) { Encipher(ref dl, ref dr); P[i] = dl; P[i + 1] = dr; }
        for (int b = 0; b < 4; b++)
            for (int k = 0; k < 256; k += 2) { Encipher(ref dl, ref dr); S[b * 256 + k] = dl; S[b * 256 + k + 1] = dr; }
    }

    /// Decipher uint32 pairs (LE) starting at byteOffset, CypherSize=(len/4)&~1 (matches server).
    public void DecipherBuffer(byte[] buf, int byteOffset)
    {
        int tmp = (buf.Length - byteOffset) / 4; tmp -= tmp % 2;
        for (int j = 0; j < tmp; j += 2)
        {
            int o = byteOffset + j * 4;
            uint xl = BinaryPrimitives.ReadUInt32LittleEndian(buf.AsSpan(o));
            uint xr = BinaryPrimitives.ReadUInt32LittleEndian(buf.AsSpan(o + 4));
            Decipher(ref xl, ref xr);
            BinaryPrimitives.WriteUInt32LittleEndian(buf.AsSpan(o), xl);
            BinaryPrimitives.WriteUInt32LittleEndian(buf.AsSpan(o + 4), xr);
        }
    }

    /// Encipher uint32 pairs (LE) starting at byteOffset (for outbound packets, M3).
    public void EncipherBuffer(byte[] buf, int byteOffset, int byteLen)
    {
        int tmp = byteLen / 4; tmp -= tmp % 2;
        for (int j = 0; j < tmp; j += 2)
        {
            int o = byteOffset + j * 4;
            uint xl = BinaryPrimitives.ReadUInt32LittleEndian(buf.AsSpan(o));
            uint xr = BinaryPrimitives.ReadUInt32LittleEndian(buf.AsSpan(o + 4));
            Encipher(ref xl, ref xr);
            BinaryPrimitives.WriteUInt32LittleEndian(buf.AsSpan(o), xl);
            BinaryPrimitives.WriteUInt32LittleEndian(buf.AsSpan(o + 4), xr);
        }
    }

