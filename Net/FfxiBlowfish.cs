    public static void DebugFirstEncipher(byte[] key)
    {
        var P = new uint[18]; var S = new uint[1024];
        for (int i = 0; i < 18; i++) P[i] = BinaryPrimitives.ReadUInt32LittleEndian(Subkey.AsSpan(i * 4));
