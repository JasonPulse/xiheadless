/// LSB's CUSTOM Blowfish (NOT standard — see src/common/blowfish.cpp).
/// The TT round function masks two of four S-box lookups with (&1)^32.
/// Verified against the compiled LSB oracle (scratchpad/dectest).
public sealed class FfxiBlowfish
{
    readonly uint[] P = new uint[18];
    readonly uint[] S = new uint[1024];

    public FfxiBlowfish(byte[] key) => Init(key);

    public string Dump() => $"P0={P[0]:x8} P1={P[1]:x8} P16={P[16]:x8} P17={P[17]:x8} S0={S[0]:x8} S1={S[1]:x8} S1023={S[1023]:x8}";

